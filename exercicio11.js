//Verificar se o número é Ímpar
let numeroimpar = parseInt(prompt("Esse número é Ímpar?"))

if (numeroimpar % 2 !== 0) {
  console.log("O número é Ímpar.");
} else {
  console.log("O número inserido não é um número ímpar, pois ele possui uma divisão exata por 2, sem deixar nenhum resto. Ou seja, ao dividirmos esse número por 2, o resultado é um número inteiro, ou seja, le é um número Par.");
}
